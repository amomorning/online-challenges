def solve(cas):
    n, k, x = inp()
    if k > x+1 or k > n:
        print(-1)
        return
    ans = list(range(k))
    mx = x
    if x == k: mx -= 1
    ans += [mx] * (n-len(ans))
    print(sum(ans))
    

cas = 1
